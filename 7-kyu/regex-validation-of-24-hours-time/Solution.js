function validateTime(time) {
    // Регулярное выражение для проверки времени в формате HH:MM
    // ^ - начало строки
    // ([01]?\d|2[0-3]) - часы: 0-9, 00-19, или 20-23
    // : - двоеточие
    // [0-5]\d - минуты: 00-59
    // $ - конец строки
    const regex = /^([01]?\d|2[0-3]):[0-5]\d$/;
    return regex.test(time);
}

